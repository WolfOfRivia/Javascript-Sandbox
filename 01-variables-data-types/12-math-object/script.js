// Get the square root of a number
x = Math.sqrt(9);
// Returns 3
console.log(x);

// Get the Absolute number
x1 = Math.abs(5);
// Returns 5
console.log(x1);
x2 = Math.abs(-5);
// Returns 5
console.log(x2);

// Round a number
x3 = Math.round(4.6);
// Returns 5
console.log(x3);
x4 = Math.round(4.2);
// Returns 4
console.log(x4);

// Round UP a number, (Numbers WILL round up no matter what.)
x5 = Math.ceil(6.1);
// Returns 7
console.log(x5);

// Round DOWN a number, (Numbers WILL round down no matter what.)
x6 = Math.floor(6.9);
// Returns 6
console.log(x6);

// Get number raised to specific power
x7 = Math.pow(2, 3);
// Returns 8
console.log(x7)

// Return the smallest of two or more numbers
x8 = Math.min(2, 6);
// Returns 2
console.log(x8)
x9 = Math.min(2, 6, 1);
// Returns 1
console.log(x9)

// Return the largest of two or more numbers
x10 = Math.max(2, 6);
// Returns 6
console.log(x10)
x11 = Math.max(2, 7, 1);
// Returns 7
console.log(x11)

// Get Random Number
x12 = Math.random();
// Returns a random decimal between  0 and 1
console.log(x12);

// Get Random decimal between 0 and 9
x13 = Math.random() * 10;
// Returns a random decimal between  0 and 1
console.log(x13);

// Get Random Number between 1 and 10
x14 = Math.floor(Math.random() * 10 + 1); // Use Math floor to make it a whole number
// Returns a random decimal between  0 and 1
console.log(x14);